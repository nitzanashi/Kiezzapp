			function clickMe() {
			if (document.getElementById('navi').style.height == "100%"){
			    document.getElementById('navi').style.height = "0%";
			    document.getElementById('navi_ul').style.display = "none";

			} else {
			    document.getElementById('navi').style.height = "100%";
			    document.getElementById('navi_ul').style.display = "block";


			  }
			}